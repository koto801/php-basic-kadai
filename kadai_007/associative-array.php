<?php
// 連想配列を作成して変数に代入
$item = [
    'name'   => 'onion',
    'price'  => 200,
    'weight' => 160,
];

// print_r関数で連想配列を出力
print_r($item);
```

**ポイント解説**

`=>` を使ってキーと値のペアを定義するのが連想配列の書き方です。例えば `'name' => 'onion'` は「nameというキーにonionという値を紐付ける」という意味です。

`print_r()` は配列の中身をキーと値がわかる形で出力してくれる関数で、出力結果はこのようになります：
```
Array
(
    [name] => onion
    [price] => 200
    [weight] => 160
)